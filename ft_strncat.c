#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	unsigned int	i_src;
	unsigned int	i_dest;

	i_src = 0;
	i_dest = ft_strlen(dest);
	while (src[i_src] && i_src < n)
	{
		dest[i_dest] = src[i_src];
		i_dest++;
		i_src++;
	}
	dest[i_dest] = '\0';
	return (dest);
}
