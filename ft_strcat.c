#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	i_src;
	int	i_dest;

	i_src = 0;
	i_dest = ft_strlen(dest);
	while (src[i_src])
	{
		dest[i_dest] = src[i_src];
		i_dest++;
		i_src++;
	}
	dest[i_dest] = '\0';
	return (dest);
}
