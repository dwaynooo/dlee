#include "libft.h"

int		ft_intlen(int n)
{
	int	res;

	res = 1;
	if (n == -2147483648)
		return (10);
	if (n < 0)
		n *= (-1);
	while (n >= 10)
	{
		n /= 10;
		res++;
	}
	return (res);
}
